package ExemploArray;

public class TesteArrayBidi {

	public static void main(String[] args) {
		String estado[][] = new String[2][2];
		
		estado[0][0] = "SP";
		estado[0][1] = "São Paulo";
		
		estado[1][0] = "RJ";
		estado[1][1] = "Rio de Janeiro";

		
	
		System.out.println(estado[0][0]);
		System.out.println(estado[0][1]);
		
	}

}
